#!/usr/bin/env bash

# ==============================================================================
# DebianXRDP-Pro
# Health Check Script
# ==============================================================================
#
# Returns:
#   0 = Healthy
#   1 = Unhealthy
#
# ==============================================================================

set -Eeuo pipefail

# ------------------------------------------------------------------------------
# Load Logger
# ------------------------------------------------------------------------------

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

source "${SCRIPT_DIR}/logger.sh"

# ------------------------------------------------------------------------------
# Check DBus
# ------------------------------------------------------------------------------

if ! pgrep -x dbus-daemon >/dev/null 2>&1; then
    log_error "DBus is not running."
    exit 1
fi

# ------------------------------------------------------------------------------
# Check XRDP
# ------------------------------------------------------------------------------

if ! pgrep -x xrdp >/dev/null 2>&1; then
    log_error "XRDP is not running."
    exit 1
fi

# ------------------------------------------------------------------------------
# Check XRDP Session Manager
# ------------------------------------------------------------------------------

if ! pgrep -x xrdp-sesman >/dev/null 2>&1; then
    log_error "XRDP Session Manager is not running."
    exit 1
fi

# ------------------------------------------------------------------------------
# Check Supervisor
# ------------------------------------------------------------------------------

if ! pgrep -x supervisord >/dev/null 2>&1; then
    log_error "Supervisor is not running."
    exit 1
fi

# ------------------------------------------------------------------------------
# Check XRDP Port
# ------------------------------------------------------------------------------

XRDP_PORT="${XRDP_PORT:-3389}"

if ! ss -ltn | grep -q ":${XRDP_PORT}"; then
    log_error "XRDP port ${XRDP_PORT} is not listening."
    exit 1
fi

# ------------------------------------------------------------------------------
# Success
# ------------------------------------------------------------------------------

log_success "Container is healthy."

exit 0
