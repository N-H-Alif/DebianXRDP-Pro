#!/usr/bin/env bash

# ==============================================================================
# DebianXRDP-Pro
# Logger Library
# ==============================================================================
#
# Description:
#   Central logging functions for all project scripts.
#
# Usage:
#   source /usr/local/bin/logger.sh
#
#   log_info "Starting service..."
#   log_success "Service started."
#   log_warning "Configuration missing."
#   log_error "Startup failed."
#
# ==============================================================================

set -Eeuo pipefail

# ------------------------------------------------------------------------------
# Colors
# ------------------------------------------------------------------------------

readonly RED="\033[0;31m"
readonly GREEN="\033[0;32m"
readonly YELLOW="\033[1;33m"
readonly BLUE="\033[0;34m"
readonly CYAN="\033[0;36m"
readonly NC="\033[0m"

# ------------------------------------------------------------------------------
# Timestamp
# ------------------------------------------------------------------------------

timestamp() {
    date +"%Y-%m-%d %H:%M:%S"
}

# ------------------------------------------------------------------------------
# Internal Logger
# ------------------------------------------------------------------------------

_log() {
    local level="$1"
    local color="$2"
    shift 2

    printf "%b[%s] [%s] %s%b\n" \
        "${color}" \
        "$(timestamp)" \
        "${level}" \
        "$*" \
        "${NC}"
}

# ------------------------------------------------------------------------------
# Public Functions
# ------------------------------------------------------------------------------

log_info() {
    _log "INFO" "${BLUE}" "$@"
}

log_success() {
    _log "SUCCESS" "${GREEN}" "$@"
}

log_warning() {
    _log "WARNING" "${YELLOW}" "$@"
}

log_error() {
    _log "ERROR" "${RED}" "$@"
}

# ------------------------------------------------------------------------------
# Banner
# ------------------------------------------------------------------------------

show_banner() {

cat << "EOF"

===========================================================
              DebianXRDP-Pro
      Production Cloud Linux Workstation
===========================================================

EOF

}

# ------------------------------------------------------------------------------
# Exit Helper
# ------------------------------------------------------------------------------

exit_with_error() {

    log_error "$1"

    exit 1

}
